export const isValidPassword = (stringPassword) => {
    return( /^(?=.*\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[^a-zA-Z0-9])(?!.*\s).{8,15}$/.test(stringPassword));
}   

export const isValidUsername = (stringUsername) => {
    return( /^(?=.*\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[^a-zA-Z0-9])(?!.*\s).{8,15}$/.test(stringUsername));
}